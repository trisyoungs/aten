// Return specific generator
Generator &GeneratorMap::generator(int gen) const
{
	if ((gen < 0) || (gen > 785))
	{
		printf("Generator id %i is out of range.\n", gen);
		return generators_[0];
	}
	return generators_[gen];
}

// Convert text string to generator id
Generator *GeneratorMap::generator(const char *s)
{
	Dnchar stripped;
	// Remove spaces and slashes from the supplied string
	stripped = stripChars(s, " /");
	// Now search for name in the generator list
	Generator *result = NULL;
	for (int n=0; n<786; n++)
	{
		if (stripped == generators_[n].name)
		{
			result = &generators_[n];
			break;
		}
	}
	return result;
}

